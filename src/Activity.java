public class Activity {
    private String activity;

    /**
     * Constructor
     * @param activity activity
     */
    public Activity(String activity) {
        this.activity = activity;
    }

    /**
     * @return Activity
     */
    public String getActivity() {
        return activity;
    }

    /**
     * Sets the activity
     * @param activity Activity
     */
    public void setActivity(String activity) {
        this.activity = activity;
    }
}
